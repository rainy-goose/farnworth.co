function toggleContact() {
    var x = document.getElementById("contactInfo");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
    }
}


function toggleWindow() {
    var x = document.getElementById("totalWindow");
    if (x.style.display === "block") {
        x.style.display = "none";
    } else {
        x.style.display = "block";
    }
}
